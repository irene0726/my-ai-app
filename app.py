import streamlit as st
import google.generativeai as genai

# 1. 呼叫雲端保險箱裡的金鑰
GOOGLE_API_KEY = st.secrets["GOOGLE_API_KEY"]
genai.configure(api_key=GOOGLE_API_KEY)

# 2. 使用最新 2.5 版本模型
model = genai.GenerativeModel('gemini-2.5-flash')

# --- 網頁前端介面設計 ---
st.set_page_config(page_title="全能口碑分析儀", page_icon="🔍")
st.title("🔍 全能口碑與危機處理分析儀")
st.markdown("一鍵切換分析視角：常規健檢、醫美專屬雷達、或是負評拆彈攻防！")

# 3. 三大模式選擇按鈕 (加入醫美專屬模式！)
mode = st.radio(
    "🎯 請選擇您需要的分析模式：",
    (
        "✅ 一般產品常規健檢 (適合美妝、3C、保健食品等)", 
        "🏥 醫美診所/醫師專屬分析 (聚焦 PTT/Dcard 醫美板真實評價)",
        "🚨 特定負評拆彈與攻防 (貼上網友原始客訴，AI 擬定對策)"
    )
)

st.divider()

# ==========================================
# 路線 A：一般產品常規健檢
# ==========================================
if mode == "✅ 一般產品常規健檢 (適合美妝、3C、保健食品等)":
    product_name = st.text_input("📦 請輸入想查詢的「產品或品牌名稱」：")
    
    if st.button("🚀 開始產品健檢"):
        if product_name:
            with st.spinner("🕵️‍♂️ AI 正在深度分析中，請稍候..."):
                try:
                    prompt = f"""
                    你是一位資深的消費者口碑分析師。請深度分析台灣各大論壇對於「{product_name}」的最新真實討論。
                    過濾掉業配文，並提供包含以下 7 點的報告：
                    1. 📊 網路聲量總結 (討論熱度與正負評比例)
                    2. ✨ 真實優點歸納 (3-5 點網友最推崇的特色)
                    3. 💣 踩雷與缺點抱怨 (不修飾的真實痛點)
                    4. 📌 各論壇風向差異 (PTT、Dcard 等平台的在意點差異)
                    5. 🚨 潛在公關危機預警 (最致命的負面標籤與平衡建議)
                    6. 🎭 素人發文切角建議 (3 個最自然的論壇討論情境)
                    7. 🎯 總結與行銷策略建議
                    """
                    response = model.generate_content(prompt)
                    st.success("分析完成！以下是產品口碑報告：")
                    st.write(response.text)
                except Exception as e:
                    st.error(f"發生錯誤：{e}")
        else:
            st.warning("⚠️ 請先輸入產品或品牌名稱喔！")

# ==========================================
# 路線 B：醫美診所/醫師專屬分析 (🌟 新增的超強模塊 🌟)
# ==========================================
elif mode == "🏥 醫美診所/醫師專屬分析 (聚焦 PTT/Dcard 醫美板真實評價)":
    clinic_doctor_name = st.text_input("🏥 請輸入想查詢的「醫美診所」或「醫師姓名」：")
    
    if st.button("🚀 開始醫美聲量分析"):
        if clinic_doctor_name:
            with st.spinner("👩‍⚕️ AI 正在 PTT Facelift 與 Dcard 醫美板深度爬文中..."):
                try:
                    prompt = f"""
                    你是一位專精於台灣醫美產業的資深口碑操盤手與公關顧問。請針對「{clinic_doctor_name}」在台灣醫美論壇 (尤其是 PTT Facelift 板、Dcard 醫美板) 的真實討論進行深度剖析。

                    請務必過濾掉明顯的診所業配、公關文與假帳號推文，並提供一份極具實戰價值的「醫美專屬口碑報告」，包含以下 6 大模塊：

                    1. 📊 醫美聲量與好感度總結
                       - 整體討論熱度如何？網友對該診所/醫師的第一印象與標籤是什麼？
                    
                    2. 👨‍⚕️ 醫師技術與美感評價 (針對醫師)
                       - 網友認為這位醫師的「強項療程」是什麼？
                       - 醫師的美感風格偏向哪一種？(例如：媽生自然風、精緻華麗風、還是保守派？)
                       - 諮詢時的態度如何？(有耐心、高冷、還是敷衍？)
                    
                    3. 🏢 診所服務與推銷壓力 (針對診所團隊)
                       - 網友對該診所的「諮詢師/美容師」評價如何？
                       - 最關鍵的痛點：是否有嚴重的「強迫推銷(推銷感重)」、「術後不理」、或「等待時間過長」等負面抱怨？
                    
                    4. ⚠️ 失敗與重修風險預警 (極度重要)
                       - 論壇上是否流傳該診所/醫師的「術後失敗」、「不自然」、「不對稱」或「產生硬塊/結節」等黑歷史文章？如果有，請具體指出是哪些療程引發的爭議。
                    
                    5. 🎭 醫美專屬：素人口碑鋪陳腳本
                       - 若要在醫美板進行口碑佈局，請提供 3 個極度真實、不具業配感的發文切角 (例如：術後修復期的焦慮分享、多間診所諮詢比較文、或是針對特定痛點的素顏對比文)。
                    
                    6. 🎯 競品比較與行銷施力點
                       - 網友在討論這位醫師/診所時，最常拿哪幾家競品診所出來比較？我們在行銷上該如何主打差異化？
                    """
                    response = model.generate_content(prompt)
                    st.success("分析完成！以下是醫美專屬口碑報告：")
                    st.write(response.text)
                except Exception as e:
                    st.error(f"發生錯誤：{e}")
        else:
            st.warning("⚠️ 請先輸入診所或醫師名稱喔！")

# ==========================================
# 路線 C：特定負評拆彈與攻防
# ==========================================
else: 
    raw_reviews = st.text_area("💬 請在此貼上網友的「原始負評」或「客訴文章」內容：", height=200)
    
    if st.button("🚀 開始負評拆彈與攻防"):
        if raw_reviews:
            with st.spinner("🚨 AI 危機處理專家正在擬定對策，請稍候..."):
                try:
                    prompt = f"""
                    你是一位擁有 15 年經驗的資深網路口碑操作專家。客戶目前遭遇了以下網友的具體負評：
                    「{raw_reviews}」

                    請提供「負評拆彈與口碑攻防作戰計畫」，包含：
                    1. 🚨 危機等級與擴散風險 (炎上指數與跟風特質)
                    2. 🌋 核心情緒與真實痛點 (底層不滿原因)
                    3. ⏳ 黃金應對行動時間表 (2小時與24小時內的具體動作)
                    4. 🩹 官方回覆與私訊溝通範本 (公開留言與私訊安撫文字)
                    5. 🛡️ 素人防守腳本 (3 個不同人設的暗樁留言切角，溫和稀釋負面)
                    6. ⚔️ 主動攻防與風向引導作戰 (風向轉移、反向質疑原 PO 動機、洗文稀釋策略)
                    7. ⚖️ 平台機制與長尾防護 (檢舉下架可行性與 SEO 防護)
                    """
                    response = model.generate_content(prompt)
                    st.success("分析完成！以下是危機處理對策：")
                    st.write(response.text)
                except Exception as e:
                    st.error(f"發生錯誤：{e}")
        else:
            st.warning("⚠️ 拆彈模式需要您先貼上網友的原始負評喔！")
